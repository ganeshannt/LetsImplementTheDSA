package com.practise.ds.linkedlist;

/**
 * @author Ganeshan Nagarajan
 * @since 09-04-2022
 */

public class Node {
    public int value;
    public Node next;

    public Node() {
        this.value = 0;
        this.next = null;
    }

    public Node(int value) {
        this.value = value;
        this.next = null;
    }
}

